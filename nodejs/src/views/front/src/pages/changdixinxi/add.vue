<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            changdimingcheng : false,
            changdiweizhi : false,
            rongnarenshu : false,
            changdizhuangtai : false,
            fuzeren : false,
            changdijieshao : false,
            changditupian : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          changdimingcheng: '',
          changdiweizhi: '',
          rongnarenshu: '',
          changdizhuangtai: '',
          fuzeren: '',
          changdijieshao: '',
          changditupian: '',
        },
        changdizhuangtaiOptions: [],
        rules: {
          changdimingcheng: [
          ],
          changdiweizhi: [
          ],
          rongnarenshu: [
          ],
          changdizhuangtai: [
          ],
          fuzeren: [
          ],
          changdijieshao: [
          ],
          changditupian: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='changdimingcheng'){
              this.ruleForm.changdimingcheng = obj[o];
              this.ro.changdimingcheng = true;
              continue;
            }
            if(o=='changdiweizhi'){
              this.ruleForm.changdiweizhi = obj[o];
              this.ro.changdiweizhi = true;
              continue;
            }
            if(o=='rongnarenshu'){
              this.ruleForm.rongnarenshu = obj[o];
              this.ro.rongnarenshu = true;
              continue;
            }
            if(o=='changdizhuangtai'){
              this.ruleForm.changdizhuangtai = obj[o];
              this.ro.changdizhuangtai = true;
              continue;
            }
            if(o=='fuzeren'){
              this.ruleForm.fuzeren = obj[o];
              this.ro.fuzeren = true;
              continue;
            }
            if(o=='changdijieshao'){
              this.ruleForm.changdijieshao = obj[o];
              this.ro.changdijieshao = true;
              continue;
            }
            if(o=='changditupian'){
              this.ruleForm.changditupian = obj[o].split(",")[0];
              this.ro.changditupian = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.changdizhuangtaiOptions = "未使用,使用中".split(',')
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('changdixinxi/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('changdixinxi/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('changdixinxi/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('changdixinxi/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      changditupianUploadChange(fileUrls) {
          this.ruleForm.changditupian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

